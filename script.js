const paths = {
  en: "/",
  "zh-hant": "/zh-hant",
  tr: "/tr",
  ms: "/ms"
};

function languageFromPath() {
  const first = window.location.pathname.split("/").filter(Boolean)[0];
  return translations[first] ? first : "en";
}

function applyLanguage(lang) {
  const dict = translations[lang] || translations.en;
  document.documentElement.lang = lang === "zh-hant" ? "zh-Hant" : lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (key && dict[key]) node.textContent = dict[key];
  });
  document.querySelectorAll("[data-lang-link]").forEach((node) => {
    const isCurrent = node.getAttribute("data-lang-link") === lang;
    node.setAttribute("aria-current", String(isCurrent));
  });
  document.title = lang === "en" ? "Ulong Technology Limited | Advanced UAV Systems" : `Ulong Technology Limited | ${dict.heroTitle}`;
}

function wireLanguageLinks() {
  document.querySelectorAll("[data-lang-link]").forEach((node) => {
    node.addEventListener("click", (event) => {
      event.preventDefault();
      const lang = node.getAttribute("data-lang-link") || "en";
      history.pushState(null, "", paths[lang]);
      applyLanguage(lang);
      window.scrollTo({ top: 0, behavior: "smooth" });
    });
  });
}

function showToast(message) {
  const toast = document.querySelector("[data-toast]");
  if (!toast) return;
  toast.textContent = message;
  toast.hidden = false;
  clearTimeout(showToast.timer);
  showToast.timer = setTimeout(() => {
    toast.hidden = true;
  }, 2200);
}

function wireCopyButtons() {
  document.querySelectorAll("[data-copy]").forEach((node) => {
    node.addEventListener("click", async () => {
      const text = node.getAttribute("data-copy") || "";
      const lang = languageFromPath();
      try {
        await navigator.clipboard.writeText(text);
      } catch {
        const textarea = document.createElement("textarea");
        textarea.value = text;
        document.body.appendChild(textarea);
        textarea.select();
        document.execCommand("copy");
        textarea.remove();
      }
      showToast((translations[lang] || translations.en).copied);
    });
  });
}

window.addEventListener("popstate", () => applyLanguage(languageFromPath()));
wireLanguageLinks();
wireCopyButtons();
applyLanguage(languageFromPath());

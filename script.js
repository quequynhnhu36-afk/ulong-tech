const pageSlugs = new Set(["products", "trade", "contact"]);

function languageFromPath() {
  const first = window.location.pathname.split("/").filter(Boolean)[0];
  return translations[first] ? first : "en";
}

function pageFromPath() {
  const parts = window.location.pathname.split("/").filter(Boolean);
  const lang = translations[parts[0]] ? parts.shift() : "en";
  const page = parts[0] || "home";
  return pageSlugs.has(page) ? page : "home";
}

function pathFor(lang, page = pageFromPath()) {
  const prefix = lang === "en" ? "" : `/${lang}`;
  if (page === "home") return prefix || "/";
  return `${prefix}/${page}`;
}

function applyLanguage(lang) {
  const dict = translations[lang] || translations.en;
  document.documentElement.lang = lang === "zh-hant" ? "zh-Hant" : lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (key && dict[key]) node.textContent = dict[key];
  });
  document.querySelectorAll("[data-lang-link]").forEach((node) => {
    const isCurrent = node.getAttribute("data-lang-link") === lang;
    node.setAttribute("aria-current", String(isCurrent));
    node.setAttribute("href", pathFor(node.getAttribute("data-lang-link") || "en"));
  });
  document.querySelectorAll("[data-route]").forEach((node) => {
    const page = node.getAttribute("data-route") || "home";
    node.setAttribute("href", pathFor(lang, page));
  });
  const titleKeys = {
    home: "heroTitle",
    products: "productsPageTitle",
    trade: "tradePageTitle",
    contact: "contactPageTitle"
  };
  const metaKeys = {
    home: "metaHome",
    products: "metaProducts",
    trade: "metaTrade",
    contact: "metaContact"
  };
  const page = pageFromPath();
  const title = dict[titleKeys[page] || "heroTitle"] || dict.heroTitle;
  const description = dict[metaKeys[page] || "metaHome"] || translations.en.metaHome;
  document.title = `Ulong Technology Limited | ${title}`;
  document.querySelector('meta[name="description"]')?.setAttribute("content", description);
  document.querySelector('meta[property="og:title"]')?.setAttribute("content", `Ulong Technology Limited | ${title}`);
  document.querySelector('meta[property="og:description"]')?.setAttribute("content", description);
}

function wireLanguageLinks() {
  document.querySelectorAll("[data-lang-link]").forEach((node) => {
    node.addEventListener("click", (event) => {
      event.preventDefault();
      const lang = node.getAttribute("data-lang-link") || "en";
      history.pushState(null, "", pathFor(lang));
      applyLanguage(lang);
      window.scrollTo({ top: 0, behavior: "smooth" });
    });
  });
}

function showToast(message) {
  const toast = document.querySelector("[data-toast]");
  if (!toast) return;
  toast.textContent = message;
  toast.hidden = false;
  clearTimeout(showToast.timer);
  showToast.timer = setTimeout(() => {
    toast.hidden = true;
  }, 2200);
}

function wireCopyButtons() {
  document.querySelectorAll("[data-copy]").forEach((node) => {
    node.addEventListener("click", async () => {
      const text = node.getAttribute("data-copy") || "";
      const lang = languageFromPath();
      try {
        await navigator.clipboard.writeText(text);
      } catch {
        const textarea = document.createElement("textarea");
        textarea.value = text;
        document.body.appendChild(textarea);
        textarea.select();
        document.execCommand("copy");
        textarea.remove();
      }
      showToast((translations[lang] || translations.en).copied);
    });
  });
}

window.addEventListener("popstate", () => applyLanguage(languageFromPath()));
wireLanguageLinks();
wireCopyButtons();
applyLanguage(languageFromPath());
